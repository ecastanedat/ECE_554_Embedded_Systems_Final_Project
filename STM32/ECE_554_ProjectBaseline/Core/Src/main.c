{
  /* USER CODE BEGIN 1 */
  SM_STATES state = INIT;

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* Enable I-Cache---------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache---------------------------------------------------------*/
  SCB_EnableDCache();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART3_UART_Init();
  MX_USB_OTG_HS_USB_Init();
  MX_LWIP_Init();
  MX_FDCAN1_Init();
  MX_TIM6_Init();
  MX_SPI1_Init();
  /* USER CODE BEGIN 2 */
  ST7735_Init(0);
  fillScreen(BLACK);
  tcp_server_init();
  testAll();

  //Start Timer
  HAL_TIM_Base_Start(&htim6);

  //Get current time
  timer_val = __HAL_TIM_GET_COUNTER(&htim6);

  //Prepare CAN data
  myTxData[0] = 0xFF;
  myTxData[1] = 0x00;
  myTxData[2] = 0xFF;
  myTxData[3] = 0x00;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  switch(state)
	  {
		  case INIT: print_to_serial("Ethernet + CAN!\r\n");
		  	  	  	 ST7735_SetRotation(2);
		  			 ST7735_WriteString(0, 0, "Hello Ethernet!", Font_11x18, RED,BLACK);
		  			 ST7735_WriteString(0, 20, "Hello CAN!", Font_11x18, RED,BLACK);
		  			 ST7735_WriteString(0, 40, "Hello SPI!", Font_11x18, RED,BLACK);
					 state = IDLE;
					 break;

		  case IDLE: //print_to_serial(globalCluster.myString);
			  	  	 ethernetif_input(&gnetif);
					 sys_check_timeouts();

					 if(__HAL_TIM_GET_COUNTER(&htim6) - timer_val >= 10000)
					 {
					   	  HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_1);
					   	  timer_val = __HAL_TIM_GET_COUNTER(&htim6);

					   	  HAL_FDCAN_AddMessageToTxFifoQ(&hfdcan1, &TxHeader, myTxData);
					   	  Toggle_CAN_Data();
					 }

					 state = IDLE;
					 break;

		  case STATE_2: break;

		  default: break;
	  }

  }
  /* USER CODE END 3 */
}

